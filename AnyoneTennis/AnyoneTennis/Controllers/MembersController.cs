using System;
using System.Collections.Generic;
using System.Data;
using System.Data.Entity;
using System.Linq;
using System.Net;
using System.Web;
using System.Web.Mvc;
using AnyoneTennis.Models;

namespace AnyoneTennis.Controllers
{
    public class MembersController : Controller
    {
        private tennisContext db = new tennisContext();

        // GET: Members
        public ActionResult Index()
        {
            return View(db.Member.ToList());
        }

        // GET: Schedules
        public ActionResult Schedules()
        {
            var scheds = (from e in db.Event
                          join c in db.Coach on e.Coach equals c.CoachId
                          select new Scheds
                          {
                              EventName = e.Name,
                              EventDescription = e.Description,
                              EventDate = e.Date,
                              Name = c.Name,
                              Nickname = c.Nickname,
                              Biography = c.Biography
                          }

                            ).ToList();


            return View(scheds);
        }

        // GET: Members/Details/5
        public ActionResult Details(int? id)
        {
            if (id == null)
            {
                return new HttpStatusCodeResult(HttpStatusCode.BadRequest);
            }
            Member member = db.Member.Find(id);
            if (member == null)
            {
                return HttpNotFound();
            }
            return View(member);
        }

        // GET: Members/Create
        public ActionResult Create()
        {
            return View();
        }

        // POST: Members/Create
        // To protect from overposting attacks, please enable the specific properties you want to bind to, for 
        // more details see https://go.microsoft.com/fwlink/?LinkId=317598.
        [HttpPost]
        [ValidateAntiForgeryToken]
        public ActionResult Create([Bind(Include = "MemberId,Name,Dob,Gender")] Member member)
        {
            if (ModelState.IsValid)
            {
                db.Member.Add(member);
                db.SaveChanges();
                return RedirectToAction("Index");
            }

            return View(member);
        }

        // GET: Members/Edit/5
